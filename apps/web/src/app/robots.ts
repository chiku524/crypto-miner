import { MetadataRoute } from 'next';
import { site } from '@/lib/site';

export default function robots(): MetadataRoute.Robots {
  return {
    rules: {
      userAgent: '*',
      allow: '/',
      disallow: ['/api/', '/dashboard/network'],
    },
    sitemap: `${site.baseUrl.replace(/\/$/, '')}/sitemap.xml`,
  };
}
